    private void Bt_envia_mensagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_envia_mensagemActionPerformed
        String msg = Tb_mensagem.getText();
        String target_nome = "";
//
        try {
            if (tipo_msg == Mensagem.MENSAGEM_CHAT_PRIVATE) {
                target_nome = L_utilizadores.getSelectedItem();
            } else if (tipo_msg == Mensagem.GRUPO_MSM) {
                target_nome = L_grupos.getSelectedItem();
            }

        } catch (ArrayIndexOutOfBoundsException e) {
            System.err.println("Erro ao selecionar " + e);
        }
        
        if (ficheiro != null) {
            long size = ficheiro.length();
            if (size < 120 * 1024 * 1024) {
                try {
                    cliente.getS_comunica().comunica_com_server(new Mensagem(Mensagem.PED_ENVIAR_FICHEIRO,cliente.getId_cliente(), utilizador_nome, target_nome, ficheiro.getName(), "Envio Ficheiro"));
                } catch (RemoteException ex) {
                    Logger.getLogger(Utilizador_menu.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {

                TA_Mensagem.append("[Application > Me] : O ficheiro Ã© demasiado grande!");

            }
        }

        if (!msg.trim().isEmpty() && !target_nome.trim().isEmpty()) {
            try {
                Tb_mensagem.setText("");
                //                                          (int REGISTO, int id_cliente, String nome_quem enviou, String quem_recebe, String msm, String aux)
                cliente.getS_comunica().comunica_com_server(new Mensagem(tipo_msg, cliente.getId_cliente(), utilizador_nome, target_nome, msg, "Mensagem"));
            } catch (RemoteException ex) {
                Logger.getLogger(Utilizador_menu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_Bt_envia_mensagemActionPerformed

